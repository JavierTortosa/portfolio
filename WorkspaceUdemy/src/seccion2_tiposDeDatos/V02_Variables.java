package seccion2_tiposDeDatos;

/*
 * VARIABLES
 * ---------
 * 
 * las variables hay que declararlas e inicializarlas
 * para declarar una variable se utiliza el tipo y un identificador
 * para inicializarla se suele usar variable = valor;
 * 
 * hay varios metodos
 * 
 * DECLARACION
 * 	int variable;
 * 
 * DECLARACION E INICIALIZACION
 *  int variable1 = 0;
 *  
 * DECLARACION MULTIPLE
 *  int variable2, variable3, variable4= 5;
 *  
 *  en este caso la variable4 ademas de declararla se inicializa con un valor de 5;
 * 
 * para los identificadores de las variables se pueden usar cualquier combinacion de:
 * 		letras
 * 		numeros 
 * 		los simbolos especiales de _ y $
 * no se pueden usar palabras reservadas de java ni la palabra "goto"
 * no pueden empezar por un numero
 * 
 */
	
public class V02_Variables {
	
	public static void main(String[] args) {
		int variable;
		
		int variable1=0;
		
		int variable2, variable3, variable4= 5;

	}
}
